-- ============================================================
-- Row Level Security (RLS)
-- Note: The backend uses the service role key which bypasses RLS.
-- These policies protect against direct client-side Supabase access.
-- ============================================================

ALTER TABLE profiles ENABLE ROW LEVEL SECURITY;
ALTER TABLE bookings ENABLE ROW LEVEL SECURITY;
ALTER TABLE booking_status_logs ENABLE ROW LEVEL SECURITY;

-- Profiles: users can only read their own profile (not all profiles)
CREATE POLICY "Users can view own profile." ON profiles
  FOR SELECT USING (auth.uid() = id);

CREATE POLICY "Users can insert their own profile." ON profiles
  FOR INSERT WITH CHECK (auth.uid() = id);

CREATE POLICY "Users can update own profile." ON profiles
  FOR UPDATE USING (auth.uid() = id);

-- Bookings: strict ownership policies
CREATE POLICY "Passengers can view own bookings." ON bookings
  FOR SELECT USING (auth.uid() = passenger_id);

CREATE POLICY "Concierges can view assigned bookings." ON bookings
  FOR SELECT USING (auth.uid() = concierge_id);

CREATE POLICY "Admins can view all bookings." ON bookings
  FOR SELECT USING (
    EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
  );

CREATE POLICY "Passengers can create own bookings." ON bookings
  FOR INSERT WITH CHECK (auth.uid() = passenger_id);

-- Booking status logs: only readable by the concierge or passenger involved
CREATE POLICY "Booking participants can view status logs." ON booking_status_logs
  FOR SELECT USING (
    EXISTS (
      SELECT 1 FROM bookings
      WHERE bookings.id = booking_status_logs.booking_id
        AND (bookings.passenger_id = auth.uid() OR bookings.concierge_id = auth.uid())
    )
  );
